package com.tutorial.basic;

public class DataTypes {

	// Java Data Types
	
	private static void dataTypeNumeric() {
		
		// byte
		byte numericByte = 3;
		System.out.println("Numeric data byte: " + numericByte);

		// short
		short numericShort = 1616;
		System.out.println("Numeric data short: " + numericShort);

		// int
		int numericDataInt = 0;
		System.out.println("Numeric data int: " + numericDataInt);
		
		// long
		long numericDataLong = 6464;
		System.out.println("Numeric data long: " + numericDataLong);

		// float
		float numericFloat = 32.32f;
		System.out.println("Numeric data float: " + numericFloat);

		// double
		double numericDouble = 64.64;
		System.out.println("Numeric data double: " + numericDouble);


	}
	
	
	public static void main(String[] args) {
		dataTypeNumeric();
	}

}
